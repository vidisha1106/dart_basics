void main()
{
  //Arithmatic Operators
  int a=5;
  int b=10;
  print(a+b);
  print(a-b);
  print(a*b);
  print(a/b);
  print(a%b);

  print(a~/b);
  print("****************");

  for(int i=0; i<10; i++)
    {
      print(i);
    }

  //augumented assignment operators
  int x=5;
  print(x += 5);
  print(x -= 5);
  print(x *= 5);
  print(x ~/= 5);
  print("****************");

  //increment and decrement
  int y=10;
  print(++y);
  print(y);

  int z=15;
  print(z++);
  print(z);

}